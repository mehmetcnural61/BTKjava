package Loops.WhileLoop;

public class Main {
    public static void main(String[] args) {
        int i=1;

    //1'den 10' tek sayılar
        while (i<10){
            System.out.println(i);
            i+=2;
        }
        System.out.println("---");
        //0'dan 10 çift sayılar
        int j=0;
        while (j<10){
            System.out.println(j);
            j+=2;
        }


    }
}
