package ConditionBlocks.IfBlocks;

public class Main {
    public static void main(String[] args) {

        int sayi=20;

        if (sayi<20){
            System.out.println("Sayi 20'den küçük");
        } else if (sayi==20) {
            System.out.println("Sayi 20'e esit !");
        }else {
            System.out.println("Sayi 20'den büyük");
        }


    }
}
